class ProvinciasCall {
  static Future<ApiCallResponse> call({
    int? idProvincias,
  }) async {
    return ApiManager.instance.makeApiCall(
      callName: 'Provincias',
      apiUrl: 'https://apis.datos.gob.ar/georef/api/provincias?',
      callType: ApiCallType.GET,
      headers: {},
      params: {},
      returnBody: true,
      encodeBodyUtf8: false,
      decodeUtf8: false,
      cache: false,
      isStreamingApi: false,
      alwaysAllowBody: false,
    );
  }

  static List<String>? idProvincia(dynamic response) => (getJsonField(
        response,
        r'''$.provincias[:].id''',
        true,
      ) as List?)
          ?.withoutNulls
          .map((x) => castToType<String>(x))
          .withoutNulls
          .toList();
  static List<String>? nombreProvincia(dynamic response) => (getJsonField(
        response,
        r'''$.provincias[:].nombre''',
        true,
      ) as List?)
          ?.withoutNulls
          .map((x) => castToType<String>(x))
          .withoutNulls
          .toList();
  static int? max(dynamic response) => castToType<int>(getJsonField(
        response,
        r'''$.parametros.max''',
      ));
}

